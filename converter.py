NODE_LIST = []
NEW_ROAD_LIST = []

for ix, road in enumerate(ROAD_LIST):
	node1 = [road[1], road[2]]
	try:
		node1Index = NODE_LIST.index(node1)
	except ValueError:
		node1Index = len(NODE_LIST)
		NODE_LIST.append(node1)
	
	node2 = [road[3], road[4]]
	try:
		node2Index = NODE_LIST.index(node2)
	except ValueError:
		node2Index = len(NODE_LIST)
		NODE_LIST.append(node2)
		
	if node1 not in NODE_LIST:
		NODE_LIST.append(node1)
	if node2 not in NODE_LIST:
		NODE_LIST.append(node2)
	
	found = False
	for i in range(len(NEW_ROAD_LIST)):
		# Find the road that has this begin point at the end of it
		if NODE_LIST[NEW_ROAD_LIST[i][-1][0]] == node1:
			# The node matches, do street name and address?
			if NEW_ROAD_LIST[i][-1][1] != road[5] or NEW_ROAD_LIST[i][-1][2] != road[0] or NEW_ROAD_LIST[i][-1][3] != road[7]:
				# Insert a point with the same node, indicating name/address change
				NEW_ROAD_LIST[i].append([node1Index, road[5], road[0], road[7]])
				
			# Now tack this point on the end
			NEW_ROAD_LIST[i].append([node2Index, road[6], road[0], road[7]])
			found = True
		
		# Or find a road that has this end point 
		if NODE_LIST[NEW_ROAD_LIST[i][0][0]] == node2:
			# The node matches, do street name and address?
			if NEW_ROAD_LIST[i][0][1] != road[6] or NEW_ROAD_LIST[i][0][2] != road[0] or NEW_ROAD_LIST[i][0][3] != road[7]:
				# Insert a point with the same node, indicating name/address change
				NEW_ROAD_LIST[i].append([node2Index, road[6], road[0], road[7]])
				
			# Now tack this point on the beginning
			NEW_ROAD_LIST[i].insert(0, [node1Index, road[5], road[0], road[7]])
			found = True
	
	if not found:
		NEW_ROAD_LIST.append([[node1Index, road[5], road[0], road[7]], [node2Index, road[6], road[0], road[7]]])

